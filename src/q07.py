# 引数(文字列)の後ろの3文字を大文字に変換して返す。
# 引数(文字列)が3文字に満たない場合はすべてを大文字に変換して返す
# 例：
# 　　end_up("Hello") → "HeLLO"
# 　　end_up("hi there") → "hi thERE"
# 　　end_up("hi") → "HI"
# 
# @param string str 文字列
# 
# @return 1つの引数(文字列)の後ろの3文字を大文字に変換して返す。
# 　　　　3文字に満たない場合はすべてを大文字に変換して返す

